package sistema.modelos;

public class Relatorio {
	private int golsA;
	private int golsB;
	private int faltasA;
	private int faltasB;
	private int cartaoA;
	private int cartaoB;
	
	public int getGolsA() {
		return golsA;
	}
	public void setGolsA(int golsA) {
		this.golsA = golsA;
	}
	public int getGolsB() {
		return golsB;
	}
	public void setGolsB(int golsB) {
		this.golsB = golsB;
	}
	public int getFaltasA() {
		return faltasA;
	}
	public void setFaltasA(int faltasA) {
		this.faltasA = faltasA;
	}
	public int getFaltasB() {
		return faltasB;
	}
	public void setFaltasB(int faltasB) {
		this.faltasB = faltasB;
	}
	public int getCartaoA() {
		return cartaoA;
	}
	public void setCartaoA(int cartaoA) {
		this.cartaoA = cartaoA;
	}
	public int getCartaoB() {
		return cartaoB;
	}
	public void setCartaoB(int cartaoB) {
		this.cartaoB = cartaoB;
	}
	
	
}
